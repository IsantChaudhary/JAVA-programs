package Loops_and_Conditions;

public class Boolean_and_Equality {
    public static void main(String[] args) {

        boolean condition1 = true;
        System.out.println(condition1);

        boolean num1 = 7 < 4;
        System.out.println(num1);

        boolean num2 = 6 > 5;
        System.out.println(num2);

        int a = 51;
        int b = 10;

        System.out.printf(" a and b are equal : %b\n", a == b);
        System.out.printf(" a is greater than b : %b\n", a > b);
    }
}
