package Loops_and_Conditions;

public class String_Methods {
    public static void main(String[] args) {

        String name = "Ishant" ;
        String surname = "Chaudhary" ;

        System.out.println(name.length());
        System.out.println(surname.length());

        System.out.println(name.equals("Ishant"));
        System.out.println(surname.equals("Chaudhary"));

        System.out.println(name.equalsIgnoreCase("IsHanT"));
        System.out.println(surname.equalsIgnoreCase("CHauDhary"));
    }
}
